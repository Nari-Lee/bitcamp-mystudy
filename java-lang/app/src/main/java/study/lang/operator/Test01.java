package study.lang.operator;

// 실습
// - 산술 연산자를 사용하여 연산을 수항핸 후 결과를 확인하라

public class Test01 {
  public static void main(String[] args) {

    System.out.println(100 + 24);
    System.out.println(100 - 24);
    System.out.println(100 * 24);
    System.out.println(100 / 24);
    System.out.println(100 % 24);
    System.out.println();

    System.out.println(100.0 + 24.0);
    System.out.println(100.0 - 24.0);
    System.out.println(100.0 * 24.0);
    System.out.println(100.0 / 24.0);
    System.out.println(100.0 % 24.0);
    System.out.println();

  }
}
